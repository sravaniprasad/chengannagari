package operations;

public class bitwise {

	public static void main(String[] args) {
	boolean a=true;
	boolean b=false;
	System.out.println("a&b is:"+(a&b));
	System.out.println("a|b is:"+(a|b));
	System.out.println("a^b is:"+(a^b));
	
	System.out.println("................................");
	int c=10;
	int d=4;
	System.out.println("c&d is:"+(c&d));
	System.out.println("c|d is:"+(c|d));
	System.out.println("c^d is:"+(c^d));
	System.out.println("~a is:"+(~d));
	
	}

}
